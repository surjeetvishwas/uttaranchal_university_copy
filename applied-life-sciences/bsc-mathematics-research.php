<div class="page-heading">
<h1>Best B.Sc. (Hons.) Mathematics with Research College in India</h1>
</div>
<p>The field of Mathematics is commonly referred to as ‘Mathematical Sciences. This field of pure and applied sciences offers the students an ideal platform to explore the mathematics and mathematical analysis. Courses in the field of Mathematics are being offered in UG, PG and Ph.D. programmes running from the School of Applied and Life Sciences.</p>
<p>B.Sc. Mathematics with Research is a 4 year degree program being offered in Mathematics discipline. It is dedicated for applied research in the field of Mathematical Sciences and further helps in enhancing research acumen of the scholar along with exposure to internship in related fields. This program has advantage of pursuing higher studies in universities around the globe. In addition, 4 years B. Sc. Mathematics with Research program focuses on research and internship.</p>
<p>During the program, students are motivated and oriented towards research. In this program we focus specially on project orientated teaching and learning processes, research and innovation of applied and pure field of mathematics. Educational visit and internship are the important and integral part of the program and follows CBCS system (Choice Based Credit System) and students also have option to opt for SWAYAM and MOOC courses. </p>
<b>Exit option as per NEP 2020 in B.Sc. Mathematics with Research</b>
<p>As per New Education Policy 2020, exit option is also available in B. Sc. (H) Mathematics with Research. Exit option should be intimidated to the University during 5th semester of the Program, so that student can opt for exit pathway during 6th semester. Thus, if a student exits B.Sc. program with research after 3 years he/ she will be awarded B. Sc. Mathematics degree, whereas if a student continues with 4 years degree program, he/she will be awarded B. Sc. Mathematics degree with Research.</p>
