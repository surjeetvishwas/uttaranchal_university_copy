<h3 class="mb-3">Glovebox</h3>
</div>
<p>A glovebox is a sealed container used to manipulate things in situations where a different atmosphere is required. Gloves are incorporated into the glovebox’s sidewalls so that the user can lay their hands inside and carry out duties without breaching containment. A section or all of the box is usually translucent to allow the user to see what is being changed. The glovebox enables a person to work with potentially dangerous substances such as radioactive materials or infectious disease agents, as well as substances that demand a very high purity inert atmosphere, such as argon or nitrogen. In a vacuum environment, a glovebox can also be used to manipulate goods.</p>
