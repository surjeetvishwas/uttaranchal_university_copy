<h3 class="mb-3">UV-Vis Spectro Photo Meter (LABMAN LMSP-UV 1900):</h3>
</div>
<p>UV-vis spectroscopy compares the amount of discrete wavelengths of UV or visible light absorbed by or transmitted through a sample to a reference or blank sample in comparison to the amount of discrete wavelengths of UV or visible light absorbed by or transmitted through a reference or blank sample. The sample makeup has an impact on this attribute, which might indicate what’s in the sample and at what concentration. Bacterial culture, drug identification, nucleic acid purity checks and quantification, beverage quality control, and chemical research are just a few of the fields where UV-vis spectroscopy is applied.</p>
