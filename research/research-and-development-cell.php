<h3 class="mb-3">X-Ray Diffractometer (XRD):</h3>
</div>
<p>XRD is a technique for determining a material’s underlying crystal structure; it can be used to verify the crystallinity and structure of a sample but does not offer chemical information. XRD patterns can be used to calculate material lattice parameters, crystal (or grain) orientation, stress in crystalline areas, and secondary phases in the sample. It’s a method of bulk characterization that produces an average diffraction pattern for the measured region. </p>
<p>XRD is a non-destructive method that can be performed at room temperature and pressure.</p>
