<h3 class="mb-3">Fourier-Transform Infra-Red (FTIR) Spectrometer (Thermoscientific Nicolet Summit LITE iD1)</h3>
</div>
<p>FTIR is a very versatile method for surface characterisation of nanoparticles. Under certain conditions, the surface chemical composition of NPs, as well as the reactive surface sites responsible for surface reactivity, may be determined. By detecting various functional groups from spectral bands, FTIR spectroscopy may be used to determine the conjugation between the nanomaterial and the adsorbed biomolecules. The frequency of vibration between atom bonds in the nanoparticle corresponds to the absorption peaks in the FTIR spectrum. The peak intensity of FTIR gives a clear indication of the components present, making it a useful tool for qualitative research.</p>
